let xPos = 0;
  const numImages = document.querySelectorAll('.img').length;

  gsap.set('.ring', { rotationY: 0, cursor: 'grab' });

  gsap.set('.img', {
    rotateY: (i) => i * (360 / numImages),
    transformOrigin: '50% 50% 500px',
    z: -500,
    backfaceVisibility: 'hidden'
  });

  let isDragging = false;

  document.querySelector('.ring').addEventListener('mousedown', (e) => {
    xPos = e.clientX;
    isDragging = true;
    gsap.set('.ring', { cursor: 'grabbing' });
  });

  window.addEventListener('mousemove', (e) => {
    if (!isDragging) return;
    let delta = e.clientX - xPos;
    gsap.to('.ring', { rotationY: '-=' + delta });
    xPos = e.clientX;
  });

  window.addEventListener('mouseup', () => {
    isDragging = false;
    gsap.set('.ring', { cursor: 'grab' });
  });
  const images = document.querySelectorAll('.img');

images.forEach((img) => {
  img.addEventListener('mouseenter', () => {
    images.forEach((other) => {
      if (other !== img) {
        other.style.opacity = 0.3; // faded
      }
    });
    img.style.opacity = 1; // fully visible
  });

  img.addEventListener('mouseleave', () => {
    images.forEach((img) => {
      img.style.opacity = 1; // reset all
    });
  });
});
